// ============ VIN CHECK ============
function handleSearch() {
    const vin = vinInp.value.trim().toUpperCase();
    if (vin.length !== 17) {
        error.style.display = "block";
        results.style.display = "none";
        loading.style.display = "none";
        return;
    }
    error.style.display = "none";
    results.style.display = "none";
    loading.style.display = "block";
    setTimeout(() => {
        loading.style.display = "none";
        showResults(vin);
    }, 1500);
}

function showResults(vin) {
    const data = {
        odometer: Math.floor(Math.random() * 300000) + 1000,
        accidents: Math.floor(Math.random() * 3),
        problems: Math.floor(Math.random() * 4),
        totalLoss: Math.random() > 0.85 ? "HAYIR" : "EVET",
        junk: Math.random() > 0.9 ? "HAYIR" : "EVET",
        titles: Math.floor(Math.random() * 3) + 1,
        sales: Math.floor(Math.random() * 5) + 1,
        recalls: Math.floor(Math.random() * 2)
    };
    document.getElementById("odometer").textContent = data.odometer.toLocaleString();
    document.getElementById("accidents").textContent = data.accidents;
    document.getElementById("problems").textContent = data.problems;
    document.getElementById("totalLoss").textContent = data.totalLoss;
    document.getElementById("junk").textContent = data.junk;
    document.getElementById("titles").textContent = data.titles;
    document.getElementById("sales").textContent = data.sales;
    document.getElementById("recalls").textContent = data.recalls;
    results.style.display = "block";
    error.style.display = "none";
}

// ============ EVENT LISTENERS ============
submitBtn.addEventListener("click", handleSearch);
vinInp.addEventListener("keypress", e => {
    if (e.key === "Enter") handleSearch();
});

langBtns.forEach(btn => {
    btn.addEventListener("click", () => {
        setLang(btn.dataset.lang);
    });
});

